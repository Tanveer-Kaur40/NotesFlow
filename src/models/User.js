const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
}, { timestamps: true });  // ✅ IMPORTANT - createdAt, updatedAt auto add hoga

module.exports = mongoose.model("User", userSchema);